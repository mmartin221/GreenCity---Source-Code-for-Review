									<p>In the quest to be a recycling guru you may be tempted to recycle every little thing you can. But small pieces like bottle caps, shredded or tiny pieces of paper, and can tabs can get stuck in the recycling processing machines. When in doubt do the credit card test. If it’s bigger than a credit card, it can be thrown in the recycling bin. If it’s smaller, toss it in the trash.

