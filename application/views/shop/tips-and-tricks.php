									<p>If you open the lid of your recycling bin and it makes you wrinkle your nose, it’s time to give it a rinse. Foul odors are usually from throwing food contaminated items in your bin, the remnants of which can continue to contaminate your recyclables. For sticky residues, you may need to give it a good scrub with dish detergent. Always turn it over to thoroughly drip dry before filling it with recyclables.</p>


 

