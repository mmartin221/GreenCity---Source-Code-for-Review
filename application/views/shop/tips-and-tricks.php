									<p>Whenever I clean out a closet, I usually find at least a few things I want to get rid of. But do you ever get that icky feeling putting something that looks perfectly good in the trash? Sure, you haven’t used it in 12 years, but does it really belong in the landfill? Don’t think of throwing it in the recycling bin to ease your guilt. The recycling gods won’t descend and turn your old bowling ball into something recyclable. It’s not going to happen. And while done with the best intentions this bad recycling habit is really counterproductive delaying sorting at the recycling facilities and clogging machinery. Things like garden hoses, old tangled strings of Christmas lights and that boombox you got back in high-school don’t belong in the recycling bin. If they’re not in bad shape, consider donating them to a local charity or thrift store so they can be reused. 

