									<!-- .entry-header -->

									<p>In my household, food and drink containers account for the majority of what ends up in the recycle bin. But did you know that putting just one food covered container in the recycle bin can contaminate an entire truckload of recyclables? So don’t toss those dirty bottles of leftover ketchup, oil, or sticky peanut butter jars into the recycling bin. Ask yourself if it’s empty, clean and dry. And when in doubt, throw it out.

 

